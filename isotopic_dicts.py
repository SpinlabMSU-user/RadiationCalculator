    z = element_dict_rev[element_symbol]
    if element[-1] == 'm':
        isomer = 1
        element = element[:-1]
    elif element[-1] == 'n':
        isomer = 2
        element = element[:-1]
    if element[-1] in '0123456789':
        a = int(element[3:])
        isomer = 0
    else:
        print(element)
        print('WHAT IS THIS: ', element[-1])
    return (z,a,isomer)
    
